package com.lbyl.Bean;

/**
 * 用户特征类
 * 
 * @author LBYL
 *
 */
public class UserInfo {

	private String name;
	private int password;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the password
	 */
	public int getPassword() {
		return password;
	}

	/**
	 * @param password
	 *            the password to set
	 */
	public void setPassword(int password) {
		this.password = password;
	}

}
